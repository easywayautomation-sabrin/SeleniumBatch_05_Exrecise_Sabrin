package DAY02_Basic_condition;

public class Pratice_04_java {

	public static void main(String[] args) {

		String name = "Sajib";

		switch (name) {
		case "Sajib":

			System.out.println("sajib");

			break;

		case "Sab":

			System.out.println("Sab");

			break;

		case "Koli":

			System.out.println("Koli");

			break;

		default:
			System.out.println("none");
			break;
		}
	}

}
